# Dictionaries
# -> It is a collection of key and pair.
# -> They are ordered and changeable.
# -> There can be no duplicates.

# A basic example of dictionary

capitals ={"Pakistan" : "Islamabad",
           "UK" : "london",
           "Spain" : "Madrid",
           "Saudi Arabia" : "Riyadh"}

print(capitals.get("India"))

# print(help(capitals)) -> It will print all the methods that we can do with a dictionary

# -> If we want to get an element from the dictionary we can get it by getting the key.
# If it is not in the dictionary then it will print none.
